def get_crypto_candles(symbol: str, resolution: str = "D", from_ts: Optional[int] = None, to_ts: Optional[int] = None) -> Dict[str, Any]:
    """
    Get crypto candles.
    
    Args:
        symbol: Crypto symbol (e.g., "BINANCE:BTCUSDT")
        resolution: 1,5,15,30,60,H,D,W,M
        from_ts: Unix timestamp start
        to_ts: Unix timestamp end
    
    Returns:
        dict: Candles data {'c': [...], 'h': [...], etc.} or error.
    """
    if from_ts is None:
        from_ts = int((time.time() - 86400 * 30) * 1000) // 1000  # 30 days ago
    if to_ts is None:
        to_ts = int(time.time())
    
    url = f"{BASE_URL}/crypto/candle"
    params = {
        "symbol": symbol,
        "resolution": resolution,
        "from": from_ts,
        "to": to_ts,
        "token": get_api_key()
    }
    try:
        resp = requests.get(url, params=params, timeout=10)
        resp.raise_for_status()
        data = resp.json()
        if data.get("s") == "no_data":
            return {"error": "No data available"}
        return data
    except requests.RequestException as e:
        return {"error": f"Request failed: {str(e)}"}
    except Exception as e:
        return {"error": f"Unexpected error: {str(e)}"}

