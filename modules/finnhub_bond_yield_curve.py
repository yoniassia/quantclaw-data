import requests
import os
import time
from datetime import datetime, timedelta
from typing import Dict, Any, Optional, List

BASE_URL = "https://finnhub.io/api/v1"

def get_api_key() -> str:
    """Get Finnhub API key from env or fallback to sandbox."""
    key = os.getenv("FINNHUB_API_KEY")
    if key:
        return key
    # Fallback sandbox key (demo data)
    return "sandbox_cskuej4r01qkhj7674h1"  # Note: Use real key from .env

def get_yield_curve() -> Dict[str, Any]:
    """
    Get US Treasury and corporate bond yield curve data.
    
    Returns:
        dict: Yield curve data or error.
    """
    url = f"{BASE_URL}/bond/yield-curve"
    params = {"token": get_api_key()}
    try:
        resp = requests.get(url, params=params, timeout=10)
        resp.raise_for_status()
        data = resp.json()
        return data
    except requests.RequestException as e:
        return {"error": f"Request failed: {str(e)}"}
    except Exception as e:
        return {"error": f"Unexpected error: {str(e)}"}

