def get_economic_calendar(from_date: Optional[str] = None, to_date: Optional[str] = None) -> List[Dict[str, Any]]:
    """
    Get economic calendar events.
    
    Args:
        from_date: Start date YYYY-MM-DD (default today)
        to_date: End date YYYY-MM-DD (default today + 7 days)
    
    Returns:
        list: Calendar events or error dict.
    """
    if from_date is None:
        from_date = datetime.now().strftime("%Y-%m-%d")
    if to_date is None:
        to_date = (datetime.now() + timedelta(days=7)).strftime("%Y-%m-%d")
    
    url = f"{BASE_URL}/calendar/economic"
    params = {"from": from_date, "to": to_date, "token": get_api_key()}
    try:
        resp = requests.get(url, params=params, timeout=10)
        resp.raise_for_status()
        data = resp.json()
        return data.get("economicCalendar", [])
    except requests.RequestException as e:
        return [{"error": f"Request failed: {str(e)}"}]
    except Exception as e:
        return [{"error": f"Unexpected error: {str(e)}"}]

